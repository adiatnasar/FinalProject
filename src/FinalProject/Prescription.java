package FinalProject;

    /// Creates prescription and store the prescription details i.e. Name, time, dose
    
public class Prescription {
	public String Name;
	public String Dose;
	public String Time;
	
	public Prescription(String Name,String Dose,String Time) {
		this.Name=Name;
		this.Dose=Dose;
		this.Time=Time;
	}
       
}
